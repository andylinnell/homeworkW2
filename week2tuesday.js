// 1. Write a function that takes an integer minutes and converts it to seconds.
const minToSec = minutes => {
    let seconds = minutes * 60;
    console.log(`${minutes} minutes is ${seconds} seconds.`);
}
minToSec(5);

// 2. Write a function that converts hours into seconds.
const hourToSec = hours => {
    let seconds = hours * 60**2;
    console.log(`${hours} hours is ${seconds} seconds.`);
}
hourToSec(22);


